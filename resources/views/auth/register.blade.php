<script>
    function openRegModal(id) {
        document.getElementById(id + '-modal').classList.remove('hidden');
        document.getElementById(id + '-modal').classList.add('flex');
    }
    function closeRegModal(id) {
        document.getElementById(id + '-modal').classList.add('hidden');
        document.getElementById(id + '-modal').classList.remove('flex');
    }

    document.addEventListener('DOMContentLoaded', () => {
        const farmerRadio = document.getElementById('role-farmer');
        const buyerRadio  = document.getElementById('role-buyer');
        const farmerLabel = document.getElementById('label-farmer');
        const buyerLabel  = document.getElementById('label-buyer');

        function updateCards() {
            if (farmerRadio.checked) {
                // Farmer selected
                farmerLabel.classList.add('border-primary', 'bg-primary-fixed/20', 'ring-2', 'ring-primary');
                farmerLabel.classList.remove('border-outline-variant/30', 'bg-surface-container-lowest', 'ring-0');
                buyerLabel.classList.remove('border-secondary', 'bg-secondary-fixed/20', 'ring-2', 'ring-secondary');
                buyerLabel.classList.add('border-outline-variant/30', 'bg-surface-container-lowest');
            } else {
                // Buyer selected
                buyerLabel.classList.add('border-secondary', 'bg-secondary-fixed/20', 'ring-2', 'ring-secondary');
                buyerLabel.classList.remove('border-outline-variant/30', 'bg-surface-container-lowest');
                farmerLabel.classList.remove('border-primary', 'bg-primary-fixed/20', 'ring-2', 'ring-primary');
                farmerLabel.classList.add('border-outline-variant/30', 'bg-surface-container-lowest');
            }
        }

        farmerRadio.addEventListener('change', updateCards);
        buyerRadio.addEventListener('change',  updateCards);

        // Auto-select role from URL param if present
        const urlRole = new URLSearchParams(window.location.search).get('role');
        if (urlRole === 'buyer') {
            buyerRadio.checked = true;
            updateCards();
        } else {
            updateCards(); // default farmer already checked via HTML
        }
    });
</script>

